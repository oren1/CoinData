    func purchasedProVersion() -> Bool {
    
        if inAppDisabled { return true }
        
        let everyMonthSubscription = CoinDataProducts.everyMonthSubscription
        let everyYearSubscription = CoinDataProducts.everyYearSubscription

        if CoinDataProducts.store.isProductPurchased(everyMonthSubscription) ||
            CoinDataProducts.store.isProductPurchased(everyYearSubscription) {
            return true
        }
        
        return false
    
    }
    
    func needToFetchAllData() -> Bool {
        
        if fetchAllData { return true }
        
        if let settings = settings {
            let fetchTime = settings.fetchDataTimeInSeconds
            let now = Date().timeIntervalSince1970
            
            if now > fetchTime { return true }
            return false
        }
        else {
            return true
        }
    }
    
    func deleteAllExceeingNotificationsAndPortfolios() {
        let realm = try! Realm()
        guard let settings = UserDataManager.shared.settings else {
            // no settings yet
            return
        }
        
        let portfoliosResults = realm.objects(Portfolio.self)
        let intervalNotificationsResults = realm.objects(IntervalNotification.self)
        let limitNotificationsResults = realm.objects(LimitNotification.self)
        
        // Delete exceeding portfolios if there are
        let maxAmountOfPortfolios = settings.maxAmountOfPortfolios
        if portfoliosResults.count > maxAmountOfPortfolios {
            let portfoliosToDelete: ArraySlice<Portfolio> = portfoliosResults.toArray()[maxAmountOfPortfolios..<portfoliosResults.count]
            let portfoliosIds = portfoliosToDelete.map({ $0._id })
            
            NetworkManager.shared.deleteMultiplePortfolios(portfoliosIds: portfoliosIds) { (error) in }
        }
        
        
        var notificationsIds = Array<String>()
        // Delete exceeding interval notifications if there are
        let maxAmountOfIntervalNotifications = settings.maxAmountOfIntervalNotifications
        if intervalNotificationsResults.count > maxAmountOfIntervalNotifications {
            let intervalNotificationsToDelete: ArraySlice<IntervalNotification> = intervalNotificationsResults.toArray()[maxAmountOfIntervalNotifications..<intervalNotificationsResults.count]
            let intervalNotificationsIds = intervalNotificationsToDelete.map({ $0._id })
            notificationsIds.append(contentsOf: intervalNotificationsIds)
        }
        
        // Delete exceeding limit notifications if there are
        let maxAmountOfLimitNotifications = settings.maxAmountOfLimitNotifications
        if limitNotificationsResults.count > maxAmountOfLimitNotifications {
            let limitNotificationsToDelete: ArraySlice<LimitNotification> = limitNotificationsResults.toArray()[maxAmountOfLimitNotifications..<limitNotificationsResults.count]
            let limitNotificationsIds = limitNotificationsToDelete.map({ $0._id} )
            notificationsIds.append(contentsOf: limitNotificationsIds)
        }
        
        NetworkManager.shared.deleteMultipleNotifications(notificationsIds: notificationsIds) { (error) in }
    }
    
    
